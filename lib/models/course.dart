class Course {
  final String id;
  final String title;
  final String description;
  final String imageUrl;
  final String tutor;
  final String duration;
  final String youtubeUrl;

  Course({
    required this.id,
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.tutor,
    required this.duration,
    required this.youtubeUrl,
  });
}
